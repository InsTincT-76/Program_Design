weekDay = input("Enter weekday:  ").lower().strip()

if weekDay == "Monday" or weekDay == "mon":
    print("Moes Monday")
    
elif weekDay == "Tuesday" or weekDay == "tues":
    print("Taco Tuesday")
    
elif weekDay == "wednesday" or weekDay == "wed":
    print("Wine Wednesday")
    
else:
    print("No deals today")